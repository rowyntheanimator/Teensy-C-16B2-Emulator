def classify_tokens(sub_tokens):
    keywords = {'int16', 'int8', 'print', 'if', 'else', 'return', 'while', 'for', 'in', 'range'}
    symbols = {'+', '-', '=', '/', ';', ':', '(', ')', '[', ']', '{', '}'}
    tokens = []
    
    for sub_token in sub_tokens:
        if sub_token == "NEWLINE":
            tokens.append(sub_token)
            continue
        if sub_token in keywords:
            tokens.append(('KEYWORD', sub_token))
        elif sub_token.isdigit():
            tokens.append(('NUMBER', sub_token))
        elif sub_token in symbols:
            tokens.append(('SYMBOL', sub_token))
        elif sub_token[-1:] == '"' and sub_token[:1] == '"':
            tokens.append(('STRING', sub_token))
        elif sub_token == "":
            continue
        else:
            tokens.append(('IDENTIFIER', sub_token))
    
    return tokens


